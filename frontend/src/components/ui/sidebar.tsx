const SidebarMenuButton = React.forwardRef<
  HTMLButtonElement,
  React.ComponentProps<"button"> & {
    asChild?: boolean
    isActive?: boolean
    tooltip?: string | React.ComponentProps<"div">
  } & VariantProps<typeof sidebarMenuButtonVariants>
>(
  (
    {
      asChild = false,
      isActive = false,
      variant = "default",
      size = "default",
      tooltip,
      className,
      children,
      ...props
    },
    ref
  ) => {
    const Comp = asChild ? Slot : "button"
    const { state } = useSidebar()
    const isCollapsed = state === "collapsed"

    const button = (
      <Comp
        ref={ref}
        data-sidebar="menu-button"
        data-active={isActive}
        className={cn(
          sidebarMenuButtonVariants({ variant, size, isActive }),
          isCollapsed && "justify-center px-0",
          className
        )}
        {...props}
      >
        {children}
      </Comp>
    )

    // Show tooltip when collapsed
    if (tooltip && isCollapsed) {
      return (
        <div className="relative group/tooltip">
          {button}
          <div className="absolute left-full ml-2 px-2.5 py-1.5 bg-popover text-popover-foreground text-sm rounded-md shadow-lg border border-border opacity-0 invisible group-hover/tooltip:opacity-100 group-hover/tooltip:visible transition-all whitespace-nowrap z-50 pointer-events-none">
            {typeof tooltip === "string" ? tooltip : tooltip?.children}
          </div>
        </div>
      )
    }

    return button
  }
)
SidebarMenuButton.displayName = "SidebarMenuButton"

